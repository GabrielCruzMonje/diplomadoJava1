
package funcionesretorno;


public class MatematicasFunciones {//inicia Clase
    
    //funcion suma
    public static int suma(int num1,int num2){//inicia metodo de tipo reterno entero       
        return num1+num2;   
    }  //termina metodo de tipo reterno entero    
    
      //funcion resta
    public static int resta(int num1,int num2){//inicia metodo de tipo reterno entero       
        return num1-num2;  
    }  //metodo de tipo reterno entero  
    
        //funcion divi
     public static int divi(int num1,int num2){//inicia metodo de tipo reterno entero       
        return num1/num2;
     }  //metodo de tipo reterno entero  
     
        //funcion multi
     public static int multi(int num1,int num2){//inicia metodo de tipo reterno entero       
        return num1 * num2;   
     } //metodo de tipo reterno entero    
        
 }//termina clase 
    

